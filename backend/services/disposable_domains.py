def normalize_email(email: str) -> str:
    """Lower-case + strip whitespace. `+tag` aliases are stripped from
    the local part so attackers can't bypass the blocklist with
    `foo+mailinator@gmail.com` (gmail delivers `foo+mailinator` to foo)."""
    e = (email or "").strip().lower()
    if "@" not in e:
        return e
    local, _, domain = e.rpartition("@")
    if "+" in local:
        local = local.split("+", 1)[0]
    return f"{local}@{domain}"


def is_disposable(email: str) -> bool:
    """True if the email's domain is in our blocklist."""
    e = normalize_email(email)
    if "@" not in e:
        return False
    domain = e.rsplit("@", 1)[1]
    return domain in BLOCKLIST